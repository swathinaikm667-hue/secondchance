# SecondChance User Stories

## As a giver
- I want to create an account so I can securely post items.
- I want to upload an item with photos, title, category, description, and location.
- I want to see my own posted items in a list.

## As a receiver
- I want to browse all available items.
- I want to search and filter items by category.
- I want to view item details and contact the giver.

## As a general user
- I want to register and log in securely.
- I want to update my profile information.
- I want to leave a review for completed item exchanges.
